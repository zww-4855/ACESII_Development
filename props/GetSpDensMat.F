      SUBROUTINE GetSpDensMat(SDENS_MAT,DENS,NSIZ,a_iterate,SCR)
C       TAKES THE OVERALL DENSITY MATRIX
C       RETURNS THE DENSITY MATRIX SPECIFIC TO A PARTICULAR ORBITAL      
C       SEE SZABO PG213
C       PURPOSE: BREAK OVERALL DENS MATRIX INTO ORBITAL CONTRIBUTIONS
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION DENS(NSIZ,NSIZ),B(nsiz,nsiz)
      real(kind=8),intent(inout):: SDENS_MAT(NSIZ,NSIZ)
      DIMENSION newDENS(Nsiz,nsiz), invDENS(nsiz,nsiz),
     &          S(nsiz,nsiz),finalDENS(nsiz,nsiz),SCR(4*nsiz*nsiz),
     &          vec(nsiz),NOCC(16), NAELEC(8), NBELEC(8)
      INTEGER NATOMS,mu,nu,NSIZ, a_iterate,NELEC!,NAELEC,NBELEC
      INTEGER BETAELEC, ALPHAELEC,nbasis,nbas
      real(kind=8)::total
      COMMON /FLAGS/  IFLAGS(100)
      COMMON /FLAGS2/ IFLAGS2(500)
      COMMON /MACHSP/ IINTLN,IFLTLN,IINTFP,IALONE,IBITWD
      COMMON /FILES/ LUOUT
      COMMON /SYMINF/ NSTART,NIRREP

C
C READ IN THE PROPERTY INTEGRALS
C
CDIR$ IVDEP
*VOCL LOOP,NOVREC
      CALL GETREC(20,"JOBARC","NIRREP  ",1,NIRREP)
       CALL GETREC(1,'JOBARC','OCCUPYA',NIRREP,NAELEC)
       CALL GETREC(20,'JOBARC','OCCUPYB',NIRREP,NBELEC) 
      CALL GETREC(20,'JOBARC','NAOBASFN',IONE,NAOTOT)

      RETURN
      END
