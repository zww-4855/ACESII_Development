      SUBROUTINE AUGMENTR(SCR, MAXCOR, ISIDE, IUHF, IRREPX)
C
C AUGMENT EXISTING SUBSPACE MATRIX - NOTE THAT THIS IS NONHERMITIAN
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION SCR(MAXCOR)
C
      PARAMETER (MAXORD=100)
C
      COMMON/EXTINF/NDIMR,IOLDEST
      COMMON/EXTRAP/MAXEXP,NREDUCE,NTOL,NSIZEC
      COMMON/RMAT/ R(10000)
C
      INDXF(I,J,N)=I+(J-1)*N
      IGET(I)=1+MOD(IOLDEST+MAXORD-I,MAXORD+1)
C
C  UPDATE R-MATRIX
C
      I000=1
      I010=I000+NSIZEC
      I020=I010+NSIZEC
      I030=I020+NSIZEC
      CALL GETLST(SCR(I000),IGET(1),1,1,ISIDE,470)
      IF(IUHF.EQ.0)THEN
       CALL SPNTSING(IRREPX,SCR(I000),SCR(I010),MAXCOR-I010+1)
      ENDIF
      DO 10 I=1,NDIMR
       CALL GETLST(SCR(I010),IGET(I),1,1,ISIDE,471)
       R(INDXF(1,I,MAXORD))=SDOT(NSIZEC,SCR(I000),1,SCR(I010),1)
10    CONTINUE 
      CALL GETLST(SCR(I000),IGET(1),1,1,ISIDE,471)
      IF(IUHF.EQ.0)THEN
       CALL SPNTSING(IRREPX,SCR(I000),SCR(I010),MAXCOR-I020+1)
      ENDIF
      DO 14 I=1,NDIMR
       CALL GETLST(SCR(I010),IGET(I),1,1,ISIDE,470)
       R(INDXF(I,1,MAXORD))=SDOT(NSIZEC,SCR(I000),1,SCR(I010),1) 
14    CONTINUE
C
#ifdef _DEBUG_LVLM
      do i=1,ndimr
      do j=1,ndimr
      call getlst(scr(i000),iget(i),1,1,iside,470)
      call getlst(scr(i010),iget(i),1,1,iside,470)
      dim=ddot(nsizec,scr(i000),1,scr(i010),1)
      write(6,"(2i4,1x,F10.5)") i,j,dim
      enddo
      enddo 
#endif 
      RETURN
      END
