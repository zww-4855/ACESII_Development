      SUBROUTINE SETLST(ICORE,MAXCOR,IUHF,IRREPX)
      IMPLICIT INTEGER (A-Z)
      DIMENSION ICORE(MAXCOR)
C
C DRIVER FOR POINTER CREATION FOR LISTS 414-419 (H INTERMEDIATES)
C
      IMODE=0
      CALL INIPCK(IRREPX,9,9,414,IMODE,0,1)
      IF(IUHF.NE.0) THEN
       CALL INIPCK(IRREPX,10,10,415,IMODE,0,1)
       CALL INIPCK(IRREPX,10,9,417,IMODE,0,1)
       CALL INIPCK(IRREPX,12,12,419,IMODE,0,1)
      ENDIF
      CALL INIPCK(IRREPX,11,11,418,IMODE,0,1)
      CALL INIPCK(IRREPX,9,10,416,IMODE,0,1)
      RETURN
      END
